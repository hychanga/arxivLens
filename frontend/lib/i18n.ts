/**
 * Plain lookup. Returns the English fallback if the key is missing in the active locale,
 * and the key itself as a last resort so missing translations stay greppable in the UI.
 */
export function t(key: string, locale: Locale): string {
  return dict[locale]?.[key] ?? dict.en[key] ?? key;
}

/** Substitutes {placeholder} occurrences in the translated string. */
export function tf(key: string, locale: Locale, vars: Record<string, string | number>): string {
  let s = t(key, locale);
  for (const [k, v] of Object.entries(vars)) {
    s = s.replaceAll(`{${k}}`, String(v));
  }
  return s;
}

/** React hook variant — most call sites use this. */
export function useT() {
  const locale = useLocaleStore((s) => s.locale);
  const fn = (key: string, vars?: Record<string, string | number>) =>
    vars ? tf(key, locale, vars) : t(key, locale);
  return Object.assign(fn, { locale });
}
